class MathTest{
   public static void main(String []args){
   
   System.out.println("SQRT: "  + Math.sqrt(37.5));
   System.out.println("SIN: "   + Math.sin(37.5));
   System.out.println("COS: "   + Math.cos(37.5));
   System.out.println("FLOOR: " + Math.floor(22.0));
   System.out.println("CEIL: "  + Math.ceil(23.0));
   System.out.println("ROUND: " + Math.round(23));
   System.out.println("MAX: "   + Math.max(71,68));
   System.out.println("MIN: "   + Math.min(71,68));
   System.out.println("RANDOM: "+ Math.random());

   
   }

}
